# include <bits/stdc++.h>
using namespace std ;
int main ()
{
    int t,i;
    cin>>t;
    while(t--)
    {
        int n;
        cin>>n;
        if (n==1) cout<<"-1"<<"\n";
        else {
            cout<<"2";
        for(i=1;i<=n-1;i++)
            cout<<"3";
        }
        cout<<"\n";
    }
}
